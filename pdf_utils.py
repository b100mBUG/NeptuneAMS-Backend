def build_attendance_pdf(
    *,
    school_name: str,
    class_name: str,
    start_date: str,
    end_date: str,
    period: str | None,
    generated_by: str,
    rows: list[tuple],
) -> bytes:
    """rows: (session_date, period, student_id, student_name, status, note)"""
    summary = _build_raw_summary(rows)
    generated_at = datetime.now().strftime("%Y-%m-%d %H:%M")
    buffer = io.BytesIO()
    doc = _make_doc(buffer, "Attendance Report")
    st = _styles()
    story: list = []
    story += _header_block(st, school_name, "Attendance Report", [
        f"<b>Class:</b> {class_name} &nbsp;&nbsp;&nbsp; "
        f"<b>Period:</b> {period or 'All'} &nbsp;&nbsp;&nbsp; "
        f"<b>Date Range:</b> {start_date} to {end_date}",
        f"<b>Generated By:</b> {generated_by} &nbsp;&nbsp;&nbsp; "
        f"<b>Generated At:</b> {generated_at}",
    ])
    story += [
        *_section(st, "Summary"),
        _summary_table(st,
            ["Total Records", "Present", "Absent", "Late", "Excused", "Attendance Rate"],
            [str(summary["total"]), str(summary["present"]), str(summary["absent"]),
             str(summary["late"]), str(summary["excused"]), summary["rate"]],
        ),
        Spacer(1, 14),
    ]
    story += _build_detail_section(st, rows)
    story += _signature_block(st)
    doc.build(story, onFirstPage=_footer, onLaterPages=_footer)
    return buffer.getvalue()


def _build_raw_summary(rows: list[tuple]) -> dict:
    total   = len(rows)
    present = sum(1 for r in rows if str(r[4]).strip().lower() == "present")
    absent  = sum(1 for r in rows if str(r[4]).strip().lower() == "absent")
    late    = sum(1 for r in rows if str(r[4]).strip().lower() == "late")
    excused = sum(1 for r in rows if str(r[4]).strip().lower() == "excused")
    rate    = (present / total * 100) if total else 0
    return {"total": total, "present": present, "absent": absent,
            "late": late, "excused": excused, "rate": f"{rate:.1f}%"}


def _build_detail_section(styles, rows: list[tuple]):
    data = [["Date", "Period", "Student ID", "Student Name", "Status", "Note"]]
    for session_date, per, sid, name, stat, note in rows[:MAX_DETAIL_ROWS]:
        data.append([
            Paragraph(session_date.isoformat(), styles["CellCenter"]),
            Paragraph(str(per), styles["CellCenter"]),
            Paragraph(str(sid), styles["CellCenter"]),
            Paragraph(str(name), styles["CellText"]),
            _status_cell(str(stat), styles),
            Paragraph((note or "")[:120], styles["CellText"]),
        ])
    if len(rows) > MAX_DETAIL_ROWS:
        data.append([
            Paragraph(f"(truncated — {len(rows)} total rows)", styles["CellText"])
        ] + [""] * 5)
    if len(data) == 1:
        data.append([Paragraph("No attendance records found", styles["CellCenter"])] + [""] * 5)
    t = Table(data, repeatRows=1,
              colWidths=[28*mm, 22*mm, 28*mm, 55*mm, 24*mm, 95*mm])
    t.setStyle(TableStyle([
        ("BACKGROUND",    (0, 0), (-1, 0), colors.HexColor(C_HEADER2)),
        ("TEXTCOLOR",     (0, 0), (-1, 0), colors.white),
        ("FONTNAME",      (0, 0), (-1, 0), "Helvetica-Bold"),
        ("FONTSIZE",      (0, 0), (-1, 0), 9),
        ("FONTSIZE",      (0, 1), (-1,-1), 8),
        ("ALIGN",         (0, 0), (2, -1), "CENTER"),
        ("VALIGN",        (0, 0), (-1,-1), "TOP"),
        ("GRID",          (0, 0), (-1,-1), 0.3, colors.HexColor("#D0D7DE")),
        ("ROWBACKGROUNDS",(0, 1), (-1,-1), [colors.white, colors.HexColor(C_ALT_ROW)]),
        ("TOPPADDING",    (0, 0), (-1,-1), 5),
        ("BOTTOMPADDING", (0, 0), (-1,-1), 5),
    ]))
    return [*_section(styles, "Attendance Details"), t, Spacer(1, 18)]


