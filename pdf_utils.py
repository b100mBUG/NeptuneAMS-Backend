def _fig_to_image(fig, width_mm: float, height_mm: float) -> Image:
    buf = io.BytesIO()
    fig.savefig(buf, format="png", dpi=150, bbox_inches="tight")
    plt.close(fig)
    buf.seek(0)
    return Image(buf, width=width_mm*mm, height=height_mm*mm)


def _bar_chart(labels, values, title, ylabel="Count",
               color=C_PRESENT, width_mm=240, height_mm=65) -> Image:
    fig, ax = plt.subplots(figsize=(width_mm/25.4, height_mm/25.4))
    x = np.arange(len(labels))
    ax.bar(x, values, color=color, edgecolor="white", linewidth=0.5)
    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=7)
    ax.set_ylabel(ylabel, fontsize=8)
    ax.set_title(title, fontsize=9, fontweight="bold")
    ax.yaxis.set_major_locator(mticker.MaxNLocator(integer=True))
    ax.grid(axis="y", linestyle="--", alpha=0.4)
    fig.tight_layout()
    return _fig_to_image(fig, width_mm, height_mm)


def _line_chart(labels, series: dict[str, list[float]], title,
                ylabel="Rate (%)", width_mm=240, height_mm=65) -> Image:
    palette = [C_PRESENT, C_ABSENT, C_LATE, C_EXCUSED, "#8E44AD", "#16A085"]
    fig, ax = plt.subplots(figsize=(width_mm/25.4, height_mm/25.4))
    x = np.arange(len(labels))
    for idx, (name, vals) in enumerate(series.items()):
        ax.plot(x, vals, marker="o", markersize=3, linewidth=1.2,
                label=name, color=palette[idx % len(palette)])
    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=7)
    ax.set_ylabel(ylabel, fontsize=8)
    ax.set_title(title, fontsize=9, fontweight="bold")
    ax.legend(fontsize=7, loc="best")
    ax.grid(linestyle="--", alpha=0.4)
    fig.tight_layout()
    return _fig_to_image(fig, width_mm, height_mm)


def _stacked_bar(labels, stacks: dict[str, list[float]],
                 stack_colors: dict[str, str], title,
                 ylabel="Records", width_mm=240, height_mm=65) -> Image:
    fig, ax = plt.subplots(figsize=(width_mm/25.4, height_mm/25.4))
    x = np.arange(len(labels))
    bottom = np.zeros(len(labels))
    for name, vals in stacks.items():
        v = np.array(vals, dtype=float)
        ax.bar(x, v, bottom=bottom, label=name,
               color=stack_colors.get(name, "#999"), edgecolor="white", linewidth=0.4)
        bottom += v
    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=7)
    ax.set_ylabel(ylabel, fontsize=8)
    ax.set_title(title, fontsize=9, fontweight="bold")
    ax.legend(fontsize=7, loc="upper right")
    ax.grid(axis="y", linestyle="--", alpha=0.4)
    fig.tight_layout()
    return _fig_to_image(fig, width_mm, height_mm)


def _pie_chart(labels, sizes, colors_list, title,
               width_mm=90, height_mm=70) -> Image:
    fig, ax = plt.subplots(figsize=(width_mm/25.4, height_mm/25.4))
    non_zero = [(l, s, c) for l, s, c in zip(labels, sizes, colors_list) if s > 0]
    if non_zero:
        ls, ss, cs = zip(*non_zero)
        ax.pie(ss, labels=ls, colors=cs, autopct="%1.1f%%",
               startangle=140, textprops={"fontsize": 7})
    ax.set_title(title, fontsize=9, fontweight="bold")
    fig.tight_layout()
    return _fig_to_image(fig, width_mm, height_mm)


def _horizontal_bar(labels, values, title, xlabel="Rate (%)",
                    width_mm=240, height_mm=80) -> Image:
    fig, ax = plt.subplots(figsize=(width_mm/25.4, height_mm/25.4))
    y = np.arange(len(labels))
    clrs = [C_ABSENT if v < 75 else C_LATE if v < 90 else C_PRESENT for v in values]
    ax.barh(y, values, color=clrs, edgecolor="white", linewidth=0.4)
    ax.set_yticks(y)
    ax.set_yticklabels(labels, fontsize=7)
    ax.set_xlabel(xlabel, fontsize=8)
    ax.set_xlim(0, 105)
    ax.axvline(75, color=C_ABSENT, linewidth=0.8, linestyle="--", label="75% threshold")
    ax.axvline(90, color=C_LATE,   linewidth=0.8, linestyle="--", label="90% threshold")
    ax.legend(fontsize=7)
    ax.set_title(title, fontsize=9, fontweight="bold")
    ax.grid(axis="x", linestyle="--", alpha=0.4)
    fig.tight_layout()
    return _fig_to_image(fig, width_mm, height_mm)


