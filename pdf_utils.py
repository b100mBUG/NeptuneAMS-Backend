"""
PDF generation utilities.

Original: build_attendance_pdf (class attendance detail — unchanged)
New:
  build_student_analysis_pdf   — full student report with charts
  build_class_analysis_pdf     — full class report with charts
  build_teacher_analysis_pdf   — teacher activity report with charts
  build_teacher_comparison_pdf — all-teachers comparison report
  build_school_overview_pdf    — school-wide overview (superadmin)

Charts are rendered with matplotlib (non-interactive Agg backend) and
embedded as PNG images inside ReportLab flowables.
"""
from __future__ import annotations

import io
from datetime import datetime
from typing import Any

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import matplotlib.ticker as mticker
import numpy as np

from reportlab.lib import colors
from reportlab.lib.enums import TA_CENTER, TA_LEFT, TA_RIGHT
from reportlab.lib.pagesizes import A4, landscape
from reportlab.lib.styles import ParagraphStyle, getSampleStyleSheet
from reportlab.lib.units import mm
from reportlab.platypus import (
    Image,
    PageBreak,
    Paragraph,
    SimpleDocTemplate,
    Spacer,
    Table,
    TableStyle,
)

# ── colour palette ─────────────────────────────────────────────────────────────
C_PRESENT = "#27AE60"
C_ABSENT  = "#E74C3C"
C_LATE    = "#F39C12"
C_EXCUSED = "#2980B9"
C_HEADER  = "#1F4E78"
C_HEADER2 = "#2C3E50"
C_ALT_ROW = "#F8F9F9"
C_META    = "#4B5563"

MAX_DETAIL_ROWS = 1000


# ══════════════════════════════════════════════════════════════════════════════
# Shared helpers
# ══════════════════════════════════════════════════════════════════════════════

def _styles():
    s = getSampleStyleSheet()
    defs = [
        ("ReportTitle",  "Heading1", "Helvetica-Bold", 18, TA_CENTER, "#1F2937"),
        ("SchoolName",   "Heading2", "Helvetica-Bold", 16, TA_CENTER, "#0F172A"),
        ("SectionTitle", "Heading3", "Helvetica-Bold", 11, TA_LEFT,   "#111827"),
        ("Meta",         "Normal",   "Helvetica",       9, TA_LEFT,   C_META),
        ("MetaRight",    "Normal",   "Helvetica",       9, TA_RIGHT,  C_META),
        ("CellText",     "BodyText", "Helvetica",       8, TA_LEFT,   "#000000"),
        ("CellCenter",   "BodyText", "Helvetica",       8, TA_CENTER, "#000000"),
        ("SmallNote",    "Normal",   "Helvetica",       7, TA_LEFT,   C_META),
    ]
    for name, parent, font, size, align, hex_color in defs:
        s.add(ParagraphStyle(
            name=name, parent=s[parent],
            fontName=font, fontSize=size,
            alignment=align,
            textColor=colors.HexColor(hex_color),
            spaceAfter=2,
        ))
    return s


def _make_doc(buffer, title: str, use_landscape: bool = True):
    ps = landscape(A4) if use_landscape else A4
    return SimpleDocTemplate(
        buffer, pagesize=ps,
        leftMargin=12*mm, rightMargin=12*mm,
        topMargin=12*mm, bottomMargin=18*mm,
        title=title, author="NeptuneAMS Analytics",
    )


def _footer(canvas, doc):
    canvas.saveState()
    canvas.setFont("Helvetica", 8)
    canvas.setFillColor(colors.grey)
    canvas.drawString(12*mm, 8*mm, "Generated by NeptuneAMS Analytics")
    w = doc.pagesize[0]
    canvas.drawRightString(w - 12*mm, 8*mm, f"Page {doc.page}")
    canvas.restoreState()


def _header_block(styles, school_name: str, report_title: str, meta_lines: list[str]):
    items = [
        Paragraph(school_name, styles["SchoolName"]),
        Paragraph(report_title, styles["ReportTitle"]),
    ]
    for line in meta_lines:
        items.append(Paragraph(line, styles["Meta"]))
    items.append(Spacer(1, 10))
    return items


def _summary_table(styles, cols: list[str], vals: list[str], total_width_mm: float = 252):
    n = len(cols)
    w = total_width_mm / n
    data = [cols, vals]
    t = Table(data, colWidths=[w*mm]*n)
    t.setStyle(TableStyle([
        ("BACKGROUND",   (0, 0), (-1, 0), colors.HexColor(C_HEADER)),
        ("TEXTCOLOR",    (0, 0), (-1, 0), colors.white),
        ("FONTNAME",     (0, 0), (-1, 0), "Helvetica-Bold"),
        ("FONTNAME",     (0, 1), (-1, 1), "Helvetica-Bold"),
        ("FONTSIZE",     (0, 0), (-1,-1), 10),
        ("ALIGN",        (0, 0), (-1,-1), "CENTER"),
        ("VALIGN",       (0, 0), (-1,-1), "MIDDLE"),
        ("BACKGROUND",   (0, 1), (-1, 1), colors.HexColor("#EAF2F8")),
        ("BOX",          (0, 0), (-1,-1), 0.6, colors.HexColor("#AAB7B8")),
        ("INNERGRID",    (0, 0), (-1,-1), 0.4, colors.HexColor("#D5D8DC")),
        ("TOPPADDING",   (0, 0), (-1,-1), 8),
        ("BOTTOMPADDING",(0, 0), (-1,-1), 8),
    ]))
    return t


def _status_cell(status: str, styles):
    s = str(status).strip().lower()
    colour_map = {"present": "green", "absent": "red", "late": "orange", "excused": "blue"}
    colour = colour_map.get(s, "black")
    return Paragraph(f'<font color="{colour}"><b>{s.title()}</b></font>', styles["CellCenter"])


def _section(styles, title: str):
    return [Paragraph(title, styles["SectionTitle"]), Spacer(1, 4)]


def _signature_block(styles):
    data = [["Prepared By:", "", "Approved By:", ""], ["", "", "", ""]]
    t = Table(data, colWidths=[30*mm, 70*mm, 30*mm, 70*mm])
    t.setStyle(TableStyle([
        ("FONTNAME",    (0, 0), (-1,-1), "Helvetica"),
        ("FONTSIZE",    (0, 0), (-1,-1), 9),
        ("LINEBELOW",   (1, 0), (1, 0), 0.5, colors.black),
        ("LINEBELOW",   (3, 0), (3, 0), 0.5, colors.black),
        ("TOPPADDING",  (0, 0), (-1,-1), 10),
    ]))
    return [Paragraph("Authorization", styles["SectionTitle"]), t]


def _apply_data_table_style(table: Table, alert: bool = False) -> None:
    header_bg = colors.HexColor(C_ABSENT if alert else C_HEADER2)
    table.setStyle(TableStyle([
        ("BACKGROUND",    (0, 0), (-1, 0), header_bg),
        ("TEXTCOLOR",     (0, 0), (-1, 0), colors.white),
        ("FONTNAME",      (0, 0), (-1, 0), "Helvetica-Bold"),
        ("FONTSIZE",      (0, 0), (-1, 0), 8),
        ("FONTSIZE",      (0, 1), (-1,-1), 8),
        ("ALIGN",         (0, 0), (-1,-1), "CENTER"),
        ("VALIGN",        (0, 0), (-1,-1), "MIDDLE"),
        ("GRID",          (0, 0), (-1,-1), 0.3, colors.HexColor("#D0D7DE")),
        ("ROWBACKGROUNDS",(0, 1), (-1,-1), [colors.white, colors.HexColor(C_ALT_ROW)]),
        ("TOPPADDING",    (0, 0), (-1,-1), 5),
        ("BOTTOMPADDING", (0, 0), (-1,-1), 5),
    ]))


