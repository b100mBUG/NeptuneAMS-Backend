# NEW — Student analysis PDF
# ══════════════════════════════════════════════════════════════════════════════

def build_student_analysis_pdf(
    *,
    school_name: str,
    student_name: str,
    student_id: str,
    generated_by: str,
    data: dict[str, Any],
) -> bytes:
    generated_at = datetime.now().strftime("%Y-%m-%d %H:%M")
    dr = data["date_range"]
    ov = data["overall"]
    buffer = io.BytesIO()
    doc = _make_doc(buffer, f"Student Analysis — {student_name}")
    st = _styles()
    story: list = []

    story += _header_block(st, school_name, "Student Attendance Analysis", [
        f"<b>Student:</b> {student_name} &nbsp;&nbsp;&nbsp; <b>ID:</b> {student_id}",
        f"<b>Date Range:</b> {dr['start']} to {dr['end']} &nbsp;&nbsp;&nbsp; "
        f"<b>Generated By:</b> {generated_by} &nbsp;&nbsp;&nbsp; <b>At:</b> {generated_at}",
    ])

    story += [
        *_section(st, "Overall Summary"),
        _summary_table(st,
            ["Total Sessions", "Present", "Absent", "Late", "Excused", "Attendance Rate"],
            [str(ov["total"]), str(ov["present"]), str(ov["absent"]),
             str(ov["late"]),  str(ov["excused"]), f"{ov['rate']}%"],
        ),
        Spacer(1, 12),
    ]

    # pie + monthly line side by side
    pie = _pie_chart(
        ["Present", "Absent", "Late", "Excused"],
        [ov["present"], ov["absent"], ov["late"], ov["excused"]],
        [C_PRESENT, C_ABSENT, C_LATE, C_EXCUSED],
        "Status Distribution", width_mm=90, height_mm=70,
    )
    if data["monthly_trend"]:
        m_labels = [r["label"] for r in data["monthly_trend"]]
        line_img = _line_chart(m_labels,
            {"Attendance Rate": [r["rate"] for r in data["monthly_trend"]]},
            "Monthly Attendance Rate (%)", width_mm=148, height_mm=70)
    else:
        line_img = Spacer(1, 1)

    sbs = Table([[pie, line_img]], colWidths=[95*mm, 153*mm])
    sbs.setStyle(TableStyle([("VALIGN", (0,0), (-1,-1), "TOP")]))
    story += [*_section(st, "Charts"), sbs, Spacer(1, 10)]

    # weekly stacked bar
    if data["weekly_trend"]:
        w_labels = [r["label"] for r in data["weekly_trend"]]
        story += [
            _stacked_bar(w_labels,
                {"Present": [r["present"] for r in data["weekly_trend"]],
                 "Late":    [r["late"]    for r in data["weekly_trend"]],
                 "Excused": [r["excused"] for r in data["weekly_trend"]],
                 "Absent":  [r["absent"]  for r in data["weekly_trend"]]},
                {"Present": C_PRESENT, "Late": C_LATE, "Excused": C_EXCUSED, "Absent": C_ABSENT},
                "Weekly Attendance Breakdown",
            ),
            Spacer(1, 10),
        ]

    # per-period table
    if data["by_period"]:
        story += _section(st, "Per-Period Breakdown")
        h = ["Period", "Total", "Present", "Absent", "Late", "Excused", "Rate"]
        pd_data = [h] + [
            [r["period"], r["total"], r["present"], r["absent"],
             r["late"], r["excused"], f"{r['rate']}%"]
            for r in data["by_period"]
        ]
        pt = Table(pd_data, colWidths=[35*mm, 25*mm, 25*mm, 25*mm, 25*mm, 25*mm, 25*mm])
        _apply_data_table_style(pt)
        story += [pt, Spacer(1, 10)]

    # absence streaks
    story += _section(st, "Worst Absence Streaks (consecutive absent days ≥ 2)")
    if data["absence_streaks"]:
        s_data = [["Start Date", "End Date", "Consecutive Days"]] + [
            [s["start"], s["end"], str(s["days"])] for s in data["absence_streaks"]
        ]
        s_tbl = Table(s_data, colWidths=[60*mm, 60*mm, 60*mm])
        _apply_data_table_style(s_tbl, alert=True)
        story += [s_tbl, Spacer(1, 10)]
    else:
        story += [Paragraph("No consecutive absence streaks detected.", st["Meta"]),
                  Spacer(1, 10)]

    # daily log
    story += [PageBreak(), *_section(st, "Full Daily Log")]
    daily = data.get("daily", [])
    d_data = [["Date", "Period", "Status", "Note"]]
    for d in daily[:MAX_DETAIL_ROWS]:
        d_data.append([
            Paragraph(d["date"],   st["CellCenter"]),
            Paragraph(d["period"], st["CellCenter"]),
            _status_cell(d["status"], st),
            Paragraph((d.get("note") or "")[:120], st["CellText"]),
        ])
    if len(daily) == 0:
        d_data.append([Paragraph("No records", st["CellCenter"]), "", "", ""])
    d_tbl = Table(d_data, repeatRows=1, colWidths=[35*mm, 30*mm, 30*mm, 152*mm])
    _apply_data_table_style(d_tbl)
    story += [d_tbl, Spacer(1, 18)]

    story += _signature_block(st)
    doc.build(story, onFirstPage=_footer, onLaterPages=_footer)
    return buffer.getvalue()


